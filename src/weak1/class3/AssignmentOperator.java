package weak1.class3;

public class AssignmentOperator {
    public static void main(String[] args) {
        int a=10;

        // =
        a=20;
        System.out.println(a);  //  20

        // +=
        a += 20;
        System.out.println(a);  //  40

        // -=
        a -= 20;
        System.out.println(a);  // 20

        // *=
        a *= 20;
        System.out.println(a);  //  400

        // /=
        a /= 20;
        System.out.println(a);  //  20

        // %=
        a %= 20;
        System.out.println(a);  //  0
    }
}
